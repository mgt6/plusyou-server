package com.openplanetideas.plusyou.server.controller;

import com.openplanetideas.plusyou.server.controller.common.AbstractController;
import org.springframework.http.HttpStatus;
import org.springframework.http.ResponseEntity;
import org.springframework.security.access.prepost.PreAuthorize;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestBody;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;

import java.util.List;

@Controller
@RequestMapping("/invite-friends")
public class InviteFriendsController extends AbstractController {

    private static final String INVITE_FRIENDS = "{facebookId}/{opportunityId}";

    @PreAuthorize("#facebookId == authentication.name")
    @RequestMapping(method = RequestMethod.POST, value = INVITE_FRIENDS)
    public ResponseEntity<Boolean> createUserInvitesInDatabase(@PathVariable final String facebookId, @PathVariable Long opportunityId, @RequestBody String friendIds) {
        List<String> friends = jsonService.extractFriendIdsFromJson(friendIds);
        userService.saveUserInvites(facebookId, opportunityId, friends);
        return new ResponseEntity<Boolean>(Boolean.TRUE, HttpStatus.OK);
    }
}